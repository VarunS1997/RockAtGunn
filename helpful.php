                    What to Do If You Know Someone is suicidal:<br/><br />

                    Youth who feel suicidal are not likely to seek help directly; however, parents, school personnel, and peers can recognize the warning signs and take immediate action to keep the youth safe. When a youth gives signs that he or she may be considering suicide, the following actions should be taken:
                    <ul>
                        <li>Remain calm.</li>
                        <li>Ask the youth directly if he or she is thinking about suicide.</li>
                        <li>Focus on your concern for their wellbeing and avoid being accusatory.</li>
                        <li>Listen.</li>
                        <li>Reassure them that there is help and they will not feel like this forever.</li>
                        <li>Do not judge.</li>
                        <li>Provide constant supervision. Do not leave the youth alone.</li>
                        <li>Remove means for self-harm.</li>
                        <li>Get help: Peers should not agree to keep the suicidal thoughts a secret and instead should tell an adult, such as a parent, teacher, or school psychologist. Parents should seek help from school or community mental health resources as soon as possible. School staff should take the student to the designated school mental health professional or administrator.</li>
